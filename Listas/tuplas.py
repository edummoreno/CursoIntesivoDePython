# ===========================
#  Trabalhando com Tuplas
# ===========================

# Tuplas são criadas com parênteses ()
# Tuplas são como listas, porém são constantes (imutáveis)

mega = (6, 4, 18, 27, 55, 56)

# Exibindo a tupla completa
print(mega)

# ===========================
#  Percorrendo uma Tupla com Loop
# ===========================

numero = (1, 2, 3, 4, 5)

# Percorrendo cada elemento da tupla usando um loop for
for n in numero:
    print(n)
